# 1. Arithmetic Operators

a = 15
b = 4

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)



# 2. Relational (Comparison) Operators

x = 10
y = 20

print("x == y:", x == y)
print("x != y:", x != y)
print("x > y:", x > y)
print("x < y:", x < y)
print("x >= y:", x >= y)
print("x <= y:", x <= y)



# 3. Logical Operators

p = True
q = False

print("p and q:", p and q)
print("p or q:", p or q)
print("not p:", not p)


# 4. Assignment Operators

num = 10

num += 5
print("After += :", num)

num -= 3
print("After -= :", num)

num *= 2
print("After *= :", num)

num /= 4
print("After /= :", num)


# 5. Membership Operators

courses = ["BA", "BCom", "BSc", "BE"]

print("BE in courses:", "BE" in courses)
print("MCA not in courses:", "MCA" not in courses)
